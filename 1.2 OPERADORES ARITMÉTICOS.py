# OPERADORES ARITMÉTICOS
# // = divisão com número inteiro
# ** = exponenciação


# OPERADORES RELACIONAIS
print(3 > 4)
print(4 >= 3)
print(1 < 2)
print(3 <= 1)
print(3 != 2) #diferente
print(3 == 4)#igual

# OPERADORES DE ATRIBUIÇÃO
a = 3
a = a + 7
print(a)
# += x _ atribuição soma
a += 5
print(a)
# *= y _ atribuição multiplicação
a *= 2
print(a)
# /= z _ atribuição divisão
a /= 4
print(a)
# %= z _ atribuição modulo
a %= 4
print(a)
# **= z _ atribuição exponenciação
a **= 8
print(a)
# //= z _ atribuição divisão inteiro
a //= 256
print(a)
